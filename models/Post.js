////////////////////////////////
// Imports
////////////////////////////////
const { Schema, model } = require("../db/connection.js")

////////////////////////////////
// Schema
////////////////////////////////
const PostSchema = new Schema(
  {
   title: String, 
   text: String, 
   author: {type: Schema.Types.ObjectId, ref: "User"},
   comments: [{type: Schema.Types.ObjectId, ref: "Comment"}]
  }, { timestamps: true }
)

////////////////////////////////
// Model
////////////////////////////////
const Post = model("Post", PostSchema)

////////////////////////////////
//  Exports
////////////////////////////////
module.exports = Post
